var appVersion = "v1.0.0"

type UI struct {
	app *app.App
}

var Logger *log.Logger

func NewGui() *UI {
	return &UI{
		app: app.New(true),
	}
}

func (u *UI) Run() {
	u.app.Run()
}

func (u *UI) Exit() {
	u.app.Exit()
}

// 获取app运行时间
var hr, min, sec int

func (u *UI) getAppRunTime() string {
	sec = sec + 1
	if sec == 60 {
		sec = 0
		min = min + 1
	}
	if min == 60 {
		min = 0
		hr = hr + 1
	}
	return fmt.Sprintf("%02d:%02d:%02d", hr, min, sec)
}
